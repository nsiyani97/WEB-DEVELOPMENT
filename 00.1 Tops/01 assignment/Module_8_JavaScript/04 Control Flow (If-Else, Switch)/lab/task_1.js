// Write a JavaScript program to check if a number is positive, negative, or zero usingan if-else statement.

// Declare a number
let num = -5;

// Check the number
if (num > 0) {
  console.log("The number is Positive");
} else if (num < 0) {
  console.log("The number is Negative");
} else {
  console.log("The number is Zero");
}

// output: The number is Negative
