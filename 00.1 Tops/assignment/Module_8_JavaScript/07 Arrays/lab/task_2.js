// Declare an array of numbers
let numbers = [10, 20, 30, 40];

// Variable to store sum
let sum = 0;

// Loop through the array
for (let i = 0; i < numbers.length; i++) {
  sum += numbers[i];
}

// Display the result
console.log("Sum of array elements:", sum);

// output: Sum of array elements: 100
